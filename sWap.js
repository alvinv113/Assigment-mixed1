function swap( a, b){
    let temp;
    console.log("The value of A before  swapping : "+ a );
    console.log("The value of  B before  swapping : "+ b );
    temp= a;
    a = b;
    b = temp;
    console.log("The value of A after  swapping : "+ a );
    console.log("The value of  B after  swapping : "+ b );
}
swap( 2,3);
